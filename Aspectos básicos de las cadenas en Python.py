# Estableciendo un valor cadena a una variable
fact = "The Moon has no atmosphere."
print(fact)

#Poniendo lo anterior como valor de otra variable pero agregandole otra cadena
fact = "The Moon has no atmosphere."
two_facts = fact + "No sound can be heard on the Moon."
print(two_facts)

#usar \n para hacer otra linea de texto
multiline = "\nFacts about the Moon:\n There is no atmosphere.\n There is no sound."
print(multiline)

#Es posible hacer varias lienas usando Comillas triples
multiline = """Facts about the Moon:
 There is no atmosphere. 
 There is no sound."""
print(multiline)